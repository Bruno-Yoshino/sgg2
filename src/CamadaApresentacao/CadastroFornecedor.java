    private void txtCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCodigoFocusLost
        if (!txtCodigo.getText().equals("0"))
        {
            fc.localizar(Integer.parseInt(txtCodigo.getText()));
            if (fc.getForn() != null)
            {
               
                txtCodigo.setText(""+fc.getForn().getCodigo());
                txtCidade.setText(fc.getForn().getCid().getNome());
                txtUf.setText(fc.getForn().getCid().getE().getSigla());
                txtNome.setText(fc.getForn().getNome());
                txtTelefone.setText(fc.getForn().getTel());
                txtCelular.setText(fc.getForn().getCel());
                txtCodCid.setText(""+fc.getForn().getCid().getCodigo());
                txtEmail.setText(fc.getForn().getEmail()); 
                txtEndereco.setText(fc.getForn().getEndereco()); 
                sc.HabilityComponents(jPanel1.getComponents(), false);
                sc.Alter(jPanel2.getComponents());
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Fornecedor não Encontrado", "Informação", JOptionPane.INFORMATION_MESSAGE);
                limpar();
                btnNovo.requestFocus();
            }
        }
    }//GEN-LAST:event_txtCodigoFocusLost

