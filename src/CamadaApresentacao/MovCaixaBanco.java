    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        if(JOptionPane.showConfirmDialog(null, "Confirama o encerramento desta Conta?", "Atenção", JOptionPane.INFORMATION_MESSAGE) == JOptionPane.YES_OPTION)
        {
            if(cc.abrirBanco())
            {
                m.InformationMessage("Encerrado com sucesso!", "Informação");
                sc.limpar(jPanel2.getComponents());
                sc.Initialize(jPanel3.getComponents());
            }
            else
            {
                m.ErroMessage("Erro", "Erro");
            }
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        switch(cc.varidar(txtValor.getText().replace(",", "."), txtNome.getText(), Integer.parseInt(txtCodigo.getText())))
        {
            case 1: m.InformationMessage("Informe o valor!", "Atenção"); txtValor.requestFocus(); txtValor.setText(""); break;
            case 2: m.InformationMessage("Valor negativo!", "Atenção"); txtValor.requestFocus();  break;
            case 3: m.InformationMessage("Informe o nome do Caixa!", "Atenção"); txtNome.requestFocus();  break;
            default:
                 if(cc.abrirBanco())
                 {
                    m.InformationMessage("Caixa aberto com sucesso!", "Informação");
                    sc.limpar(jPanel2.getComponents());
                    sc.Initialize(jPanel3.getComponents());
                 }
                 else
                 {
                     m.ErroMessage("Erro ao abriri Caixa!", "Atenção");
                 }
        }
    }//GEN-LAST:event_btnGravarActionPerformed

